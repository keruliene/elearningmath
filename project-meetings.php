            <h3 class="font-weight-bold text-center pb-3">Meeting minutes</h3>
            <h4 class="font-weight-bold pt-5">07.03.2018/ Day No.1</h4>
            <p class="text-justify">
                At first participants of the project meeting got acquainted with VCVTC having a tour at the school
                premises and watching presentation prepared by the project coordinator of VCVTC Rainer Kuutma. After
                then the mathematics teachers presented the results of testing e-exercises and theoretical explanation
                material. Estonians hadn’t finished the testing yet, so only Finns and Latvians could share their
                experience. The greatest part of Finnish students admitted that they like e-materials on Kahoot internet
                platform. They also found rather exciting the competitive side of Kahoot. The PIC students think that it
                is possible to learn something from Kahoot.
            </p>
            <p class="text-justify">
                Some e-exercises caused problems for the Latvian students, but they really enjoyed doing tasks on Kahoot
                internet platform. The RSTS students also liked to compete in Kahoot and they found e-exercises rather
                attractive. The students even asked to the mathematics teacher to give them exercises on Kahoot, so
                excited they were. Unfortunately many students didn’t read theoretical explanation material the teacher
                had prepared. If they had read it, very possible the theory would helped them to do e-exercises.
            </p>
            <p class="text-justify">
                The next meeting task was to develop the second inquiry form in order to check results in mathematics
                for the 1st year students. The teachers decided to develop this form on the basis of the first inquiry
                form. There were added several questions about e-environment, Kahoot platform and other e-issues to the
                second inquiry form. The teachers have to do this survey during April and analyse the results.
            </p>
            <h4 class="font-weight-bold pt-5">08.03.2018/ Day No.2</h4>
            <p class="text-justify">
                During the second meeting day project participants developed support material/short guidance on using
                Kahoot platform for other teachers. This support material consists of simple instructions with nice
                pictures how to develop exercises on Kahoot internet platform. There will be developed also instructions
                for using similar internet platform.
            </p>
            <p class="text-justify">
                In the end of meeting project participants talked about the final conference that will be held in Riga
                on May 23. There will take part all teachers, project manager and coordinators, as well as students in
                the conference. RSTS will invite also mathematics teachers from other vocational schools and
                representative of the Latvian Nordplus administration. Arita as Latvian project coordinator will tell
                about the project in general. Kristīne will introduce with the project website. The teachers and
                students will share their experience in the project and present the project results. The teachers have
                to include 3 slides about the school in their presentation. The duration of presentation for the
                teachers have to be 10-15 minutes. The students have to talk approximately 5 minutes. The first school
                that participate will be VCVTC, the next will be PIC and as the last ones their experience will share
                RSTS teachers and students.
            </p>
