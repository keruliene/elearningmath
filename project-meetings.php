            <h3 class="font-weight-bold text-center pb-3">Meeting minutes</h3>
            <h4 class="font-weight-bold pt-5">08.11.2017/ Day No.1</h4>
            <p class="text-justify">
                In the first meeting day PIC was presented, its curriculum and the students' viewpoint on the
                college and study opportunities. Over the course of the day, the project participants – RSTS, PIC
                and VCVTC presented the results of the questionnaire. As a result of the mathematics teachers'
                presentations, the data of all the participant countries questionnaires were compared and the common
                issues affecting all learners on the most challenging study topics were assessed and discussed. The
                results of the survey once again confirmed that the problems of mathematics acquisition are similar
                in all countries. For learners, mathematics seems to be difficult, often they do not see its link
                with real life, but at the same time, learners believe that mathematics is important and needed in
                their future professions. The students’ answers highlighted that they would love to master
                mathematics. Many suggested ideas for more attractive and interesting teaching materials. The
                results of the proposed mathematics tasks have revealed that the worst outcomes are obtained solving
                percentages, unit measurements and different equations.
            </p>
            <p class="text-justify">
                Later, the PIC teachers introduced their college premises and study directions. The seminar
                participants went on a college tour to get acquainted with school, classrooms, training and leisure
                activities.
            </p>
            <p class="text-justify">
                Over the course of the day, teachers from the project participant countries introduced the e-study
                environments they use at their educational establishments and countries. The representatives agreed
                on topics for which teachers in each project participant country would draw up their mathematics
                assignments in the Kahoot internet platform:
            </p>
            <ul>
                <li>Riga State Technical School – tasks for transition from one measurement unit to another;</li>
                <li>Porvoo International College – quadratic equations;</li>
                <li>Valga Vocational Education Training Centre – solving of percentage tasks.</li>
            </ul>
            <p class="text-justify">
                It was decided that the tasks should be tied to social situations that would be binding on the
                students in order to see the link between mathematics and real life.
            </p>
            <p class="text-justify">
                While mathematics teachers discussed their issues, project manager and three coordinators talked
                about and planned the next meeting in Valga on March 7-8 in 2018. There were also discussions about
                the documentation on project expenses for travel, board and lodging and meeting organization as
                well.
            </p>
            <p class="text-justify">
                In the evening all project participants and representatives of PIC had a tasty dinner in the nice
                restaurant in old town.
            </p>
            <h4 class="font-weight-bold pt-5">09.11.2017/ Day No.2</h4>
            <p class="text-justify">
                The day began with an interesting and useful presentation about digitalization and IT tools in
                education of PIC by Katriina.
            </p>
            <p class="text-justify">
                The representatives of the project participant countries presented the tasks they had set up in the
                Kahoot internet platform. The participants discussed the tasks that were developed and discussed the
                creation of the explanatory material for the theoretical material and tasks to be connected, and
                their link with possible e-study environments.
            </p>
            <p class="text-justify">
                At the end of the meeting, the results of the work concluded were summarized, and it was decided
                until the next meeting in the VCVTC, to offer learners to calculate the above tasks using the
                previously developed theoretical material in order to assess the progress of the students and make
                the necessary conclusions.
            </p>
