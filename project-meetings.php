            <h4 class="font-weight-bold pt-5">13.09.2017/ Day No.1</h4>
            <p class="text-justify">
                The partner meeting and working seminar started with registration and welcoming coffee. After then
                all
                participants from all three institutions introduced to themselves. There were participating project
                manager,
                coordinators and teachers in this meeting. Project manager Sandra Bidzāne from RSTS told about the
                project,
                its aims, activities, results etc. Project coordinators Arita Ošiņa (RSTS) and Rainer Kuutma (VCVTC)
                as well
                as teacher Katriina Karkimo (PIC) presented their school and experience in e-studies, and told about
                working
                with e-learning materials. There are rather many differences among these three schools. RSTS has the
                biggest
                number of students and staff, there are also many specialities provided in RSTS. PIC and VCVTC are
                smaller
                educational institutions, but they are more experienced in working in e-environment and with
                e-learning
                materials than RSTS. All partner schools are working in Moodle. PIC is using different interactive
                on-line
                tools to make learning more attractive and interesting for their students. RSTS and VCVTC mainly are
                using
                PowerPoint presentations, lessons notes, different materials in PDF and tests as e-learning
                materials.
                Mathematics teachers Elita Kazakēviča (RSTS), Alexander Kainberg (PIC) and Sille Allik (VCVTC) told
                about
                teaching mathematics in their schools. There are also many differences. RSTS in the only school with
                final
                state examinations, which are the same as for the gymnasium and secondary school students. PIC and
                VCVTC
                haven’t final examinations at all. There is a huge difference in amount of mathematics lessons. RSTS
                has the
                biggest one. Also the most complicated mathematics topics are in RSTS.
            </p>
            <p class="text-justify">
                In conclusion of the first day project manager and coordinators discussed about the project budget
                and
                project money for partners that RSTS hasn’t transferred yet due to complicated system in state
                institutions
                of Latvia.
            </p>
            <p class="text-justify">
                Late in the evening project participants went to the Folk club ALA, where had a nice dinner.
            </p>
            <h4 class="font-weight-bold pt-5">14.09.2017/ Day No.2</h4>
            <p class="text-justify">The second day began with the partner meeting and conversations about project
                documentation, reports and next activities in Porvoo. At the beginning coordinators from PIC and
                VCVTC were unpleasantly surprised about all the complicated reporting that offered RSTS according to
                its previous experience in international projects. As coordinator from RSTS Arita Ošiņa went to the
                Nordplus seminar on September 15, she found out that it is possible to use more simple reporting and
                documentation. So project partners don’t have to make agreements on voluntary work, it will be
                enough with timesheets in the end of the project. Partners don’t have to submit originals of the
                invoices, tickets, cheques etc. regarding the travel and accommodation, it is enough with the copies
                and one report with all travel and accommodation costs for every meeting.</p>
            <p class="text-justify">During the first part of working seminar teachers started to work with
                development of inquiry form for the students. Teachers of every school worked in the groups and
                wrote down some questions for this form. After then each group presented its questions and all
                seminar participants discussed about these questions. All seminar participants agreed on Katriina’s
                idea to divide the inquiry form in several parts. The first is background information, like gender,
                age and study program. The second part is about attitudes towards mathematics and studies in
                general. The third part is about materials used in learning mathematics. Then come 10 mathematics
                exercises. The last question is about student’s ideas for development new means for learning
                mathematics.</p>
            <p class="text-justify">The last question is about student’s ideas for development new means for
                learning mathematics.
                In the second part of working seminar all 5 teachers worked together and wrote down mathematics
                exercises. At the beginning teachers had written down more than 10 exercises, but then all decided
                to reduce the number to 10 in order to have the inquiry form rather short and concrete. Katriina put
                all questions in Google Form as all had decided before. The Google Form allows to get a summary of
                all responses that is needed for this project.</p>
            <h4 class="font-weight-bold pt-5">15.09.2017/ Day No.3</h4>
            <p class="text-justify">There was only working seminar in the third meeting day, but project manager and
                coordinators from RSTS and VCVTC also participated in order to help their teachers with English.</p>
            <p class="text-justify">In the first part of day teachers worked in groups – Estonian teachers together
                with Latvian teachers and both Swedish teachers in one group. They had to discuss about the form and
                content of the e-learning material in mathematics that has to be developed within the project. After
                lunch teachers presented their first ideas. Alexander from PIC showed 3 internet platforms where is
                possible to make different kind of exercises, tests etc. in attractive form. Estonian teacher showed
                one more platform. It was decided to use these 4 platforms for the development of e-learning
                material. All teachers got a homework to make at least 3 exercises in one or several of these
                platforms. These exercises should be developed based on the results of students’ survey in each
                school. Teachers have to present the exercises or explanation material during the next meeting in
                Porvoo in November 2017.</p>
            <p class="text-justify">In the conclusion of the meeting project manager and coordinator from RSTS
                informed all participants about the tasks and deadlines until the meeting in Porvoo on November
                8-10, 2017.</p>
